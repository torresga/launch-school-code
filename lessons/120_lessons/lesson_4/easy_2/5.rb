# There are a number of variables listed below. What are the different types and how do you know which is which?

# This is a local variable. We can tell because there are no extra symbols to denote it is anything other than a local variable.
excited_dog = "excited dog"
# This is an instance variable. We can tell because it has one @ symbol in front of its' name.
@excited_dog = "excited dog"
# This is a class variable. We can tell because it has two @ symbols in front of its' name.
@@excited_dog = "excited dog"
