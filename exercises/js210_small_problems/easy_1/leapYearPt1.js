// Leap Years Part 1
// In the modern era under the Gregorian Calendar, leap years occur in every year that is evenly divisible by 4, unless the year is also divisible by 100. If the year is evenly divisible by 100, then it is not a leap year, unless the year is also evenly divisible by 400.
//
// Assume this rule is valid for any year greater than year 0. Write a function that takes any year greater than 0 as input, and returns true if the year is a leap year, or false if it is not a leap year.

// Input: a year
// Output: a boolean representing whether the year is true or false

// Algorithm
// If year is divisible by 4
// if year is divisible by 100
//   if year is divisible by 400
//     return true
//   else return false
// else return true
// Else return false

function isLeapYear(year) {
  if (year % 4 === 0) {
    if (year % 100 === 0) {
      if (year % 400 === 0) {
        return true;
      } else {
        return false;
      }
    } else {
      return true;
    }
  } else {
    return false;
  }
}

// Further Exploration
// The order in which you perform tests for a leap year calculation is important. For what years will isLeapYear fail if you rewrite it as shown below?

function isLeapYear(year) {
  if (year % 100 === 0) {
    return false;
  } else if (year % 400 === 0) {
    return true;
  } else {
    return year % 4 === 0;
  }
}

// 2400, 240000, 2000, 400. Because years divisible by 400 are also divisible by 100, which returns false.

// Examples:

console.log(isLeapYear(2016));      // true
